<h1>
	About Mood Tracker
</h1>

<p>Mood tracker is a web application that was created for the Web Application Development course at University of Pisa.
	</p>
	<p>It uses <a href="https://kit.svelte.dev/">Svelte-kit</a> as a front-end framework, and <a href="https://firebase.google.com/">Firebase</a> as a back-end. The User Interface is made with the help of <a href="https://sveltematerialui.com/">Svelte Material UI</a>.
</p>
<p>
Its objective is to provide insight on the user's daily moods and what may influence them.
</p>
